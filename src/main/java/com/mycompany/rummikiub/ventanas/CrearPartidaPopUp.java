    private void btnCrearPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearPartidaActionPerformed
        if (txfNombrePartida.getText().length() < 1){
            JOptionPane.showMessageDialog(this, "El nombre de partida no puede estar vacÃ­a");
            return;
        }
        String nombrePartida = txfNombrePartida.getText();
        int cantidadJugadores = Integer.parseInt(cbxCantidadJugadores.getSelectedItem().toString());
        DataOutputStream salida = null;
        try {
            salida = new DataOutputStream(socket.getOutputStream());
            salida.writeInt(0004);
            salida.writeUTF(nombrePartida);
            salida.writeInt(cantidadJugadores);
        } catch (IOException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
        
        this.dispose();
    }//GEN-LAST:event_btnCrearPartidaActionPerformed

